package io.robonews.console.dto;

public class DataResponse<T> {

    private T data;

    private boolean error = false;

    public boolean isError() {
        return error;
    }

    public void setError(boolean error) {
        this.error = error;
    }

    public T getData() {
        return data;
    }

    public void setData(T response) {
        this.data = response;
    }
}
